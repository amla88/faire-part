CREATE OR REPLACE FUNCTION public.get_stats_admin()
 RETURNS jsonb
 LANGUAGE plpgsql
 SECURITY DEFINER
 SET search_path TO 'public'
AS $function$
declare
  v_rsvp_total int := 0;
  v_rsvp_apero int := 0;
  v_rsvp_repas int := 0;
  v_rsvp_soiree int := 0;
  v_photos_pending int := 0;
  v_photos_approved int := 0;
  v_photos_rejected int := 0;
  v_musiques_pending int := 0;
  v_musiques_approved int := 0;
  v_musiques_rejected int := 0;
begin
  -- RSVP (optional)
  if to_regclass('public.rsvp') is not null then
    select count(*) into v_rsvp_total from public.rsvp;
    select count(*) into v_rsvp_apero from public.rsvp where coalesce(pour_apero,false) is true;
    select count(*) into v_rsvp_repas from public.rsvp where coalesce(pour_repas,false) is true;
    select count(*) into v_rsvp_soiree from public.rsvp where coalesce(pour_soiree,false) is true;
  end if;

  -- Photos (optional)
  if to_regclass('public.photos') is not null then
    select count(*) into v_photos_pending from public.photos where status = 'pending';
    select count(*) into v_photos_approved from public.photos where status = 'approved';
    select count(*) into v_photos_rejected from public.photos where status = 'rejected';
  end if;

  -- Musiques (optional)
  if to_regclass('public.musiques') is not null then
    select count(*) into v_musiques_pending from public.musiques where status = 'pending';
    select count(*) into v_musiques_approved from public.musiques where status = 'approved';
    select count(*) into v_musiques_rejected from public.musiques where status = 'rejected';
  end if;

  return jsonb_build_object(
    'rsvp_total', v_rsvp_total,
    'rsvp_apero', v_rsvp_apero,
    'rsvp_repas', v_rsvp_repas,
    'rsvp_soiree', v_rsvp_soiree,
    'photos_pending', v_photos_pending,
    'photos_approved', v_photos_approved,
    'photos_rejected', v_photos_rejected,
    'musiques_pending', v_musiques_pending,
    'musiques_approved', v_musiques_approved,
    'musiques_rejected', v_musiques_rejected
  );
end;
$function$
;


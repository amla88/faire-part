CREATE OR REPLACE FUNCTION public.ensure_avatar_for_personne(p_personne_id bigint)
 RETURNS public.avatars
 LANGUAGE plpgsql
 SECURITY DEFINER
 SET search_path TO 'public'
AS $function$
DECLARE
    existing_avatar avatars;
BEGIN
    -- Chercher un avatar existant
    SELECT * INTO existing_avatar
    FROM avatars
    WHERE personne_id = p_personne_id;
    
    -- Si pas d'avatar, en créer un
    IF existing_avatar IS NULL THEN
        INSERT INTO avatars (personne_id)
        VALUES (p_personne_id)
        RETURNING * INTO existing_avatar;
    END IF;
    
    RETURN existing_avatar;
END;
$function$
;


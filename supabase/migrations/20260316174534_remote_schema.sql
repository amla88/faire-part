CREATE OR REPLACE FUNCTION public.get_admin_stats()
 RETURNS jsonb
 LANGUAGE plpgsql
 SECURITY DEFINER
 SET search_path TO 'public'
AS $function$
DECLARE
    result jsonb := '{}';
BEGIN
    -- Vérifier que l'utilisateur est admin
    IF NOT is_admin() THEN
        RAISE EXCEPTION 'Accès refusé';
    END IF;
    
    -- Construire les statistiques
    SELECT jsonb_build_object(
        'familles', (SELECT COUNT(*) FROM familles),
        'personnes', (SELECT COUNT(*) FROM personnes),
        'avatars_configures', (SELECT COUNT(*) FROM avatars WHERE personne_id IS NOT NULL),
        'musiques_totales', (SELECT COUNT(*) FROM musiques),
        'confirmations', jsonb_build_object(
            'reception', (SELECT COUNT(*) FROM personnes WHERE present_reception = true),
            'repas', (SELECT COUNT(*) FROM personnes WHERE present_repas = true),
            'soiree', (SELECT COUNT(*) FROM personnes WHERE present_soiree = true)
        ),
        'invitations_par_type', (
            SELECT jsonb_object_agg(invite_pour, cnt)
            FROM (
                SELECT invite_pour, COUNT(*) as cnt
                FROM personnes 
                GROUP BY invite_pour
            ) subq
        )
    ) INTO result;
    
    RETURN result;
END;
$function$
;

CREATE OR REPLACE FUNCTION public.get_avatar_for_token(p_token text, p_personne_id bigint)
 RETURNS TABLE(id bigint, personne_id bigint, seed text, options jsonb, created_at timestamp with time zone, updated_at timestamp with time zone)
 LANGUAGE plpgsql
 SECURITY DEFINER
AS $function$
DECLARE
  fam RECORD;
BEGIN
  SELECT * INTO fam FROM get_famille_by_token(p_token);
  IF fam IS NULL THEN
    RETURN;
  END IF;

  RETURN QUERY
  SELECT a.id, a.personne_id, a.seed, a.options, a.created_at, a.updated_at
  FROM avatars a
  WHERE a.personne_id = p_personne_id
    AND EXISTS (
      SELECT 1 FROM personnes p WHERE p.id = a.personne_id AND p.famille_id = fam.id
    );
END;
$function$
;


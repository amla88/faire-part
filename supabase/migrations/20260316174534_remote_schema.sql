CREATE OR REPLACE FUNCTION public.mcp_describe_table(p_table text, p_schema text DEFAULT 'public'::text)
 RETURNS json
 LANGUAGE sql
 STABLE
AS $function$
  select coalesce(
    json_agg(row_to_json(c)), '[]'::json
  )
  from (
    select
      c.ordinal_position,
      c.column_name,
      c.data_type,
      c.is_nullable,
      c.character_maximum_length,
      c.numeric_precision,
      c.numeric_scale,
      c.column_default
    from information_schema.columns c
    where c.table_schema = p_schema and c.table_name = p_table
    order by c.ordinal_position
  ) c;
$function$
;


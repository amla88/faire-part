CREATE OR REPLACE FUNCTION public.upsert_avatar_choices(p_personne_id bigint, p_selections jsonb)
 RETURNS void
 LANGUAGE plpgsql
 SECURITY DEFINER
 SET search_path TO 'public'
AS $function$
DECLARE
    avatar_record avatars;
    category_key text;
    asset_id_val bigint;
BEGIN
    -- Assurer qu'un avatar existe
    SELECT * INTO avatar_record FROM ensure_avatar_for_personne(p_personne_id);
    
    -- Supprimer les choix existants
    DELETE FROM avatar_asset_choices WHERE avatar_id = avatar_record.id;
    
    -- Insérer les nouveaux choix
    FOR category_key IN SELECT jsonb_object_keys(p_selections)
    LOOP
        asset_id_val := (p_selections ->> category_key)::bigint;
        
        INSERT INTO avatar_asset_choices (avatar_id, category, asset_id)
        VALUES (avatar_record.id, category_key::avatar_category, asset_id_val);
    END LOOP;
END;
$function$
;

CREATE OR REPLACE FUNCTION public.upsert_avatar_choices(p_personne_id integer, p_selections jsonb)
 RETURNS void
 LANGUAGE plpgsql
 SECURITY DEFINER
 SET search_path TO 'public'
AS $function$
declare
  v_avatar_id int;
  v_key text;
  v_val text;
begin
  select id into v_avatar_id from public.avatars where personne_id = p_personne_id;
  if v_avatar_id is null then
    insert into public.avatars(personne_id) values (p_personne_id) returning id into v_avatar_id;
  end if;

  -- Iterate jsonb object and upsert rows
  for v_key, v_val in
    select key, value::text from jsonb_each(p_selections)
  loop
    insert into public.avatar_asset_choices(avatar_id, category, asset_id)
    values (v_avatar_id, v_key, v_val)
    on conflict (avatar_id, category)
    do update set asset_id = excluded.asset_id;
  end loop;
end;
$function$
;

CREATE OR REPLACE FUNCTION public.upsert_avatar_for_token(p_token text, p_personne_id bigint, p_seed text, p_options jsonb)
 RETURNS jsonb
 LANGUAGE plpgsql
 SECURITY DEFINER
AS $function$
DECLARE
  fam RECORD;
  result jsonb;
BEGIN
  -- validate token using existing SECURITY DEFINER RPC get_famille_by_token
  SELECT * INTO fam FROM get_famille_by_token(p_token);
  IF fam IS NULL THEN
    RAISE EXCEPTION 'invalid_token';
  END IF;

  -- ensure the personne belongs to the famille
  IF NOT EXISTS (SELECT 1 FROM personnes WHERE id = p_personne_id AND famille_id = fam.id) THEN
    RAISE EXCEPTION 'personne_not_in_famille';
  END IF;

  INSERT INTO avatars(personne_id, seed, options)
  VALUES (p_personne_id, p_seed, COALESCE(p_options, '{}'::jsonb))
  ON CONFLICT (personne_id) DO UPDATE
    SET seed = EXCLUDED.seed,
        options = EXCLUDED.options,
        updated_at = now()
  RETURNING to_jsonb(avatars.*) INTO result;

  RETURN result;
END;
$function$
;

